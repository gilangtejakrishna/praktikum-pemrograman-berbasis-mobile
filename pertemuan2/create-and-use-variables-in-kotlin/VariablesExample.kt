fun main() {
    // Deklarasi variabel menggunakan var (mutable)
    var name: String = "Gilang"
    var age: Int = 21

    // Menampilkan nilai variabel
    println("Nama: $name")
    println("Usia: $age tahun")

    // Mengubah nilai variabel
    name = "Gilang Teja Krishna"
    age = 22

    // Menampilkan nilai variabel yang sudah diubah
    println("Nama lengkap: $name")
    println("Usia tahun depan: $age tahun")

    // Deklarasi variabel menggunakan val (immutable)
    val university: String = "UTDI"

    // Menampilkan nilai variabel immutable
    println("Universitas: $university")

}

// Output
// Nama: Gilang
// Usia: 21 tahun
// Nama lengkap: Gilang Teja Krishna
// Usia tahun depan: 22 tahun
// Universitas: UTDI

